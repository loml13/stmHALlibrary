/**
  * @brief  OLED初始化
  * @param  无
  * @retval 无
  */
void OLED_Init(void)
{
    // 延时确保OLED稳定
    HAL_Delay(100);
    
    // 初始化序列
    OLED_WriteCommand(0xAE); // 关闭显示
    OLED_WriteCommand(0x20); // 设置内存地址模式
    OLED_WriteCommand(0x10); // 00,水平地址模式;01,垂直地址模式;10,页地址模式;11,无效
    OLED_WriteCommand(0xB0); // 设置页地址
    OLED_WriteCommand(0xC8); // 设置COM扫描方向
    OLED_WriteCommand(0x00); // 设置低列地址
    OLED_WriteCommand(0x10); // 设置高列地址
    OLED_WriteCommand(0x40); // 设置起始行地址
    OLED_WriteCommand(0x81); // 设置对比度控制寄存器
    OLED_WriteCommand(0xFF); // 对比度值
    OLED_WriteCommand(0xA1); // 设置段重定义
    OLED_WriteCommand(0xA6); // 设置正常显示
    OLED_WriteCommand(0xA8); // 设置多路复用比
    OLED_WriteCommand(0x3F); // 1/64 duty
    OLED_WriteCommand(0xA4); // 全局显示开启
    OLED_WriteCommand(0xD3); // 设置显示偏移
    OLED_WriteCommand(0x00); // 无偏移
    OLED_WriteCommand(0xD5); // 设置显示时钟分频比/振荡器频率
    OLED_WriteCommand(0xF0); // 设置分频比
    OLED_WriteCommand(0xD9); // 设置预充电周期
    OLED_WriteCommand(0x22); // 
    OLED_WriteCommand(0xDA); // 设置COM硬件管脚配置
    OLED_WriteCommand(0x12); // 
    OLED_WriteCommand(0xDB); // 设置VCOMH
    OLED_WriteCommand(0x20); // 0.77*VCC
    OLED_WriteCommand(0x8D); // 设置DC-DC使能
    OLED_WriteCommand(0x14); // 
    OLED_WriteCommand(0xAF); // 开启显示
    
    OLED_Clear();
}

/**
  * @brief  安全的OLED清屏函数
  * @retval 1表示成功，0表示失败
  */
uint8_t OLED_Safe_Clear(void)
{
    if (OLED_Test_Connection() != HAL_OK) {
        return 0;
    }
    
    OLED_Clear();
    return 1;
}

